package com.deepvision.notify.Util

enum class NoteFiltering {
    /**
     * Do not filter notes.
     */
    ALL_NOTES,

    /**
     * Filters only the work notes.
     */
    WORK_NOTES,

    /**
     * Filters only the school notes.
     */
    SCHOOL_NOTES,

    /**
     * Filters only the shopping notes.
     */

    SHOPPING_NOTES,

    /**
     * Filters only the lifestyle notes.
     */

    HEALTH_LIFESTYLE,

    /**
     * Filters only the random notes.
     */

    RANDOM_NOTES
}